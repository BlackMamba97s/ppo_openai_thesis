import torch
import torch.nn as nn
from config import args


class Net(nn.Module):
    """
    Actor-Critic Network for PPO, consisting of a CNN and fully connected layers for the actor and critic heads.

    Attributes:
        cnn_base: A sequential neural network module containing the convolutional layers of the network.
        v: A sequential neural network module representing the value function head.
        fc: A sequential neural network module representing the fully connected layers of the network.
        alpha_head: A sequential neural network module representing the alpha parameter of the beta distribution.
        beta_head: A sequential neural network module representing the beta parameter of the beta distribution.
    """
    def __init__(self):
        super(Net, self).__init__()
        self.cnn_base = nn.Sequential(  # input shape (4, 96, 96)
            nn.Conv2d(args.img_stack, 8, kernel_size=4, stride=2),
            nn.ReLU(),  # activation
            nn.Conv2d(8, 16, kernel_size=3, stride=2),  # (8, 47, 47)
            nn.ReLU(),  # activation
            nn.Conv2d(16, 32, kernel_size=3, stride=2),  # (16, 23, 23)
            nn.ReLU(),  # activation
            nn.Conv2d(32, 64, kernel_size=3, stride=2),  # (32, 11, 11)
            nn.ReLU(),  # activation
            nn.Conv2d(64, 128, kernel_size=3, stride=1),  # (64, 5, 5)
            nn.ReLU(),  # activation
            nn.Conv2d(128, 256, kernel_size=3, stride=1),  # (128, 3, 3)
            nn.ReLU(),  # activation
        )  # output shape (256, 1, 1)
        self.v = nn.Sequential(nn.Linear(256, 100), nn.ReLU(), nn.Linear(100, 1))
        self.fc = nn.Sequential(nn.Linear(256, 100), nn.ReLU())
        self.alpha_head = nn.Sequential(nn.Linear(100, 3), nn.Softplus())
        self.beta_head = nn.Sequential(nn.Linear(100, 3), nn.Softplus())
        self.apply(self._weights_init)

    @staticmethod
    def _weights_init(m):
        """
        Initialize the weights of the network using Xavier initialization and bias values of 0.1 for the convolutional layers.

        Args:
            m: A module of the network.
        """
        if isinstance(m, nn.Conv2d):
            nn.init.xavier_uniform_(m.weight, gain=nn.init.calculate_gain('relu'))
            nn.init.constant_(m.bias, 0.1)

    def forward(self, x):
        """
        Forward pass of the network.

        Args:
            x: The input tensor.

        Returns:
            A tuple containing the alpha and beta parameters of the beta distribution and the value function estimate.
        """
        x = self.cnn_base(x)
        x = x.view(-1, 256)
        v = self.v(x)
        x = self.fc(x)
        alpha = self.alpha_head(x) + 1
        beta = self.beta_head(x) + 1

        return (alpha, beta), v
